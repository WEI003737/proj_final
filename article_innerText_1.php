            <h4 class="cin_h4">想靠運動瘦身，時間和力氣當然要用在刀口上，有10種運動被認為最燃脂，而且如果能夠「混搭」，效果最好。</h4>
            <p>大多數研究都同意，以重量訓練搭配健康飲食，可有效燃燒脂肪，如果每週再加上中至高強度的有氧運動，燃脂效果更加乘。
                可見減重單靠重訓或有氧運動，效果都不如「混搭」來得好，專家建議這10種燃脂效果最好的運動，其中重訓和有氧都有。有的在戶外鍛練，有的去一趟健身房就能一次滿足，可選擇最適合自己的，搭配出黃金瘦身運動組合。</p>

            <h4 class="cin_h4">1. 跑步</h4>
            <p>跑步是最快速也最簡單的燃脂運動之一，尤其衝刺型的短跑能強化核心肌力，並且在短時間內達到高強度，特別適合沒耐心的人。
                慢跑雖然對身體負擔較小，但快速的短跑能挑戰極限，習慣這樣的強度之後，能進一步提高體能。</p>
            <div class="cin_zoomImage">
                <img src="./img/article_innerText_1b.jpg" alt="">
            </div>

            <h4 class="cin_h4">2. 跳繩</h4>
            <p>以體重140磅（63公斤）的女性來說，每跳繩30分鐘可燃燒318卡路里。跳繩還能運動全身，包括大腿的股四頭肌、髖部的臀大肌，以及核心肌肉都能得到鍛練。另外提升身體的平衡與協調性，除了減重，對心肺也有幫助。
            </p>
            <div class="cin_zoomImage">
                <img src="./img/article_innerText_1c.jpg" alt="">
            </div>

            <h4 class="cin_h4">3. 重量訓練</h4>
            <p>新陳代謝從30歲開始下滑，重訓鍛鍊出的肌肉能加速新陳代謝、燃燒更多脂肪，但好處還不只於此。
                重訓能幫助預防骨質疏鬆，曾有研究顯示，不論年輕人或老年人，經過1年左右的重訓，骨骼中礦物質的含量都明顯上升；即使是70歲以上的長者，經長時間的重訓後，骨質密度也有效地增加。</p>
            <div class="cin_zoomImage">
                <img src="./img/article_innerText_1d.jpg" alt="">
            </div>

            <h4 class="cin_h4">4. 拳擊</h4>
            <p>拳擊能同時鍛練手臂、腰、肩、臀、大腿和小腿，動作的同時大腦也必須跟著思考，是身心一起投入的全身性運動。
                拳擊還能訓練身體的協調性與耐力，更有利於未來進行其他運動。另外有助於紓壓、釋放內心憤怒，英國哈利王子就曾靠拳擊走出母親黛安娜王妃過世的情緒低谷</p>
            <div class="cin_zoomImage">
                <img src="./img/article_innerText_1e.jpg" alt="">
            </div>

            <h4 class="cin_h4">5. 飛輪</h4>
            <p>如果不喜歡跑步，飛輪是相對低衝擊的替代選擇。只要姿勢正確，可一次鍛練核心、大腿、臀部，同樣屬於全身性的運動。
                飛輪主要鍛練的都是身體最大塊、最強健的肌肉，能釋放出荷爾蒙，製造出更多肌肉，效果有點像是重量訓練。</p>
            <div class="cin_zoomImage">
                <img src="./img/article_innerText_1f.jpg" alt="">
            </div>
            <h4 class="cin_h4">6. 高強度間歇訓練</h4>
            <p>高強度間歇訓練被認為是目前為止最燃脂、也最能提升新陳代謝的運動之一，而且需要的時間非常短，可能只花費10分鐘，網路上還一度盛行只要7分鐘的高強度間歇訓練。
                以7分鐘運動為例，每個動作運動時間30秒，中間休息10秒，再換下一個動作，做完12個動作為一個循環。
                高強度間歇訓練能增加肌肉量與肌肉中的酵素活性，讓人有更多肌肉去燃燒卡路里，而且燃脂的時間點主要發生在運動結束後。還有研究顯示能燃燒腹部脂肪，有望降低心血管疾病風險。</p>
            <div class="cin_zoomImage">
                <img src="./img/article_innerText_1g.jpg" alt="">
            </div>
            <h4 class="cin_h4">7. 划船機</h4>
            <p>健身房的划船機能一次鍛練大腿股四頭肌、臀大肌、核心、手臂與背部，同時結合有氧運動與重量訓練，當然是燃脂的首選之一。
                尤其很多坐辦公室的上班族，可能長時間在桌前拱著背，划船機也能藉由伸展脊椎、臀部與肩膀來矯正姿勢。</p>
            <div class="cin_zoomImage">
                <img src="./img/article_innerText_1h.jpg" alt="">
            </div>
            <h4 class="cin_h4">8. 滑步機</h4>
            <p>滑步機也是健身房常見的心肺耐力訓練工具，雖然看起來很輕鬆，但只要調高阻力，就能安全又有效地燃脂。
                更曾有研究指出，滑步機提升的心跳速率甚至比跑步機高。只要用對滑步機，不只可以鍛練股四頭肌、大腿後側肌群、臀肌、小腿肌，如果把手握在握把上，還能運動手臂、胸肌及背肌。</p>
            <h4 class="cin_h4">9. 戰繩（Battling Ropes）</h4>
            <P>有別於一般有氧運動，戰繩是少數能利用上半身進行的有氧訓練，因此有「健胸神器」之稱。
                戰繩必須把繩子甩出波型，乍看像是用手腕、手臂發力，但其實所有力量都來自軀幹，髖關節是主要發動力量的部位。因為動作需要全身肌肉協作，操作戰繩能鍛練身體協調性，是門檻低又能顯著提升心肺功能、肌力與肌耐力的運動。
            </P>
            <div class="cin_zoomImage">
                <img src="./img/article_innerText_1i.jpg" alt="">
            </div>
            <h4 class="cin_h4">10. 游泳</h4>
            <p>最後一個是游泳，如果不喜歡跑步帶來的負荷感，游泳也是相對低衝擊的替代選項。水增加阻力能鍛練肌肉，還會運動到身體幾乎每一部份，同時提高心率、改善心臟健康，加上較不需要承受重量，特別適合關節炎患者。</p>

